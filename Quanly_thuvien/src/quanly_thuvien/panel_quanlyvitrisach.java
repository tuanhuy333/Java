    private void btnThemvitrisachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemvitrisachActionPerformed

        int ma = Integer.parseInt(txtMavitri.getText());
        String ke = txtke.getText();
        String khu = txtkhu.getText();
        String ngan = txtngan.getText();
        
        String sql=String.format("insert into vitrisach(mavitri,khu,ke,ngan) values(%d,N'%s',N'%s',N'%s')",ma,khu,ke,ngan);
        try{

            c.insert_remove_updateData(sql);//thực hiện thêm

            JOptionPane.showMessageDialog(this, "Thêm thành công");

        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Thêm thất bại !"+e);
        }
    }//GEN-LAST:event_btnThemvitrisachActionPerformed

