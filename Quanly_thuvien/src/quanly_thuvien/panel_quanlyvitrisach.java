    private void jTablevitriMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablevitriMouseClicked
        btnThemvitrisach.setEnabled(false);

        String ma=maRowSelected();

        ResultSet rs=c.loadData("select * from vitrisach where mavitri="+ma);

        //xoa de gan lai gia tri ứng với csdl

        try {
            if(rs.next()){
                txtMavitri.setText(String.valueOf(rs.getInt(1)));
                txtkhu.setText(rs.getString(2));
                txtke.setText(rs.getString(3));
                 txtngan.setText(rs.getString(4));

            }} catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Lỗi khi click vào table");
            }
    }//GEN-LAST:event_jTablevitriMouseClicked

