    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
      clearDataInTable();
        try {
            String gttimkiem=txtTimkiem.getText();
            
            String sql="select * from loaisach where tenloaisach like N'%"+gttimkiem+"%' or maloaisach like '%"+gttimkiem+"%'";
            ResultSet rs=c.loadData(sql);
            
            while(rs.next()){
                
                dtm.addRow(new Object[]{
                    rs.getInt(1),rs.getString(2)
                });
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(panel_quanlysach.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed
     public void hienthidata(){
       try {
                ResultSet rs=c.loadData("select * from loaisach");
             
            while(rs.next()){
                dtm.addRow(new Object[]{
                   rs.getInt(1),rs.getString(2)
                });
                
            }
           
        } catch (SQLException ex) {
            
            JOptionPane.showMessageDialog(this, "Không có dữ liệu để hiển thị");
        }
    }
    public void clearDataInTable(){
        for(int i=dtm.getRowCount()-1;i>=0;--i){
            dtm.removeRow(i);
        }
    }  
    public String maRowSelected(){
        
        
         int row=jTableloaisach.getSelectedRow();
         String ma=this.jTableloaisach.getModel().getValueAt(row, 0).toString();
         
         return ma;
    }

