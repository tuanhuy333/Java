    private void btnThemloaisachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemloaisachActionPerformed

        int ma = Integer.parseInt(txtMaloaisach.getText());
        String ten = txtTenloaisach.getText();

        String sql=String.format("insert into loaisach(maloaisach,tenloaisach) values(%d,N'%s')",ma,ten);
        try{

            c.insert_remove_updateData(sql);//thực hiện thêm

            JOptionPane.showMessageDialog(this, "Thêm thành công");

        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Thêm thất bại !"+e);
        }
    }//GEN-LAST:event_btnThemloaisachActionPerformed

