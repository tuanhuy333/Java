    private void jTableloaisachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableloaisachMouseClicked
        btnThemloaisach.setEnabled(false);

        String ma=maRowSelected();

        ResultSet rs=c.loadData("select * from loaisach where maloaisach="+ma);

        //xoa de gan lai gia tri ứng với csdl

        try {
            if(rs.next()){
                txtMaloaisach.setText(String.valueOf(rs.getInt(1)));
                txtTenloaisach.setText(rs.getString(2));

            }} catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Lỗi khi click vào table");
            }
    }//GEN-LAST:event_jTableloaisachMouseClicked

